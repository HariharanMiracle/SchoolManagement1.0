package model;


public class Marks {
    
    String Sid;
    String studentid;
    String m;
    String g;
    String t;

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getG() {
        return g;
    }

    public void setG(String g) {
        this.g = g;
    }

    public String getSid() {
        return Sid;
    }

    public void setSid(String Sid) {
        this.Sid = Sid;
    }

    public String getStudentid() {
        return studentid;
    }

    public void setStudentid(String studentid) {
        this.studentid = studentid;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

   
}